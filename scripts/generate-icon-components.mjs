#!/usr/bin/env node
/**
 * Generate one tree-shakable React component per SVG in assets/icons/.
 *
 * Output:
 *   components/icons/types.ts                 — IconProps alias
 *   components/icons/icon-<kebab>.tsx         — one component per SVG
 *   components/icons/index.ts                 — alphabetical re-exports
 *
 * Conventions (per Plan: Icon Management Policy v1):
 *   - PascalCase exported name prefixed with "Icon" (e.g. IconAccountBoxLine)
 *   - kebab-case file name prefixed with "icon-" (e.g. icon-account-box-line.tsx)
 *   - fill="#14151A" hardcoded color → fill="currentColor" so the icon
 *     inherits the parent text color in light/dark themes
 *   - <svg width="1em" height="1em" …> so callers can size with font-size
 *   - {...props} spreads on the root <svg> so consumer can pass className,
 *     aria-label, role, style, onClick, etc.
 *
 * Re-run when assets/icons/ changes:
 *   npm run icons:components
 */
import { promises as fs } from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const ROOT = path.resolve(__dirname, '..')
const SRC_DIR = path.join(ROOT, 'assets', 'icons')
const OUT_DIR = path.join(ROOT, 'components', 'icons')

const HARDCODED_COLOR_RE = /#14151[Aa]/g

const HEADER = `// AUTO-GENERATED by scripts/generate-icon-components.mjs. Do not edit by hand.
// Source SVG: assets/icons/{filename}
`

function toComponentName(stem) {
  // "account-box-line" → "IconAccountBoxLine"
  // "4k-fill"          → "Icon4kFill"
  // "Base Heading"     → "IconBaseHeading"
  const parts = stem.split(/[\s\-_]+/).filter(Boolean)
  const camel = parts
    .map((p) => p.charAt(0).toUpperCase() + p.slice(1))
    .join('')
  return 'Icon' + camel
}

function toKebabBase(stem) {
  return stem
    .toLowerCase()
    .replace(/[\s_]+/g, '-')
    .replace(/[^a-z0-9-]/g, '')
    .replace(/-+/g, '-')
    .replace(/^-|-$/g, '')
}

function rewriteSvg(svg) {
  let s = svg.trim()

  // Strip XML/comment noise
  s = s.replace(/<\?xml[^?]*\?>/g, '').replace(/<!--[\s\S]*?-->/g, '')

  // currentColor swap (handles both fill and stroke)
  s = s.replace(HARDCODED_COLOR_RE, 'currentColor')

  // Convert SVG attributes that JSX requires camelCase
  s = s
    .replace(/\bfill-rule=/g, 'fillRule=')
    .replace(/\bclip-rule=/g, 'clipRule=')
    .replace(/\bstroke-width=/g, 'strokeWidth=')
    .replace(/\bstroke-linecap=/g, 'strokeLinecap=')
    .replace(/\bstroke-linejoin=/g, 'strokeLinejoin=')
    .replace(/\bstroke-dasharray=/g, 'strokeDasharray=')
    .replace(/\bstroke-dashoffset=/g, 'strokeDashoffset=')
    .replace(/\bstroke-miterlimit=/g, 'strokeMiterlimit=')
    .replace(/\bstroke-opacity=/g, 'strokeOpacity=')
    .replace(/\bfill-opacity=/g, 'fillOpacity=')
    .replace(/\bstop-color=/g, 'stopColor=')
    .replace(/\bstop-opacity=/g, 'stopOpacity=')
    .replace(/\bxlink:href=/g, 'xlinkHref=')

  // Rewrite root <svg ...> to a React-friendly opening tag while preserving viewBox
  s = s.replace(/<svg([^>]*)>/, (_, attrs) => {
    const viewBoxMatch = attrs.match(/viewBox=["']([^"']+)["']/)
    const viewBox = viewBoxMatch ? viewBoxMatch[1] : '0 0 24 24'
    return `<svg width="1em" height="1em" viewBox="${viewBox}" fill="currentColor" xmlns="http://www.w3.org/2000/svg" {...props}>`
  })

  return s
}

function buildComponentSource(componentName, svgJsx, sourceFile) {
  return `${HEADER.replace('{filename}', sourceFile)}
import type { SVGProps } from 'react'

export function ${componentName}(props: SVGProps<SVGSVGElement>) {
  return (
    ${svgJsx.split('\n').map((line, i) => (i === 0 ? line : '    ' + line)).join('\n')}
  )
}
`
}

async function main() {
  const start = Date.now()

  // Clean only the generated files (icon-*.tsx, types.ts, index.ts), not
  // any sibling files. On case-insensitive filesystems the folder may be
  // shared with other content; rm -rf would nuke that.
  await fs.mkdir(OUT_DIR, { recursive: true })
  for (const f of await fs.readdir(OUT_DIR)) {
    if (f.startsWith('icon-') && f.endsWith('.tsx')) {
      await fs.rm(path.join(OUT_DIR, f))
    } else if (f === 'index.ts' || f === 'types.ts') {
      await fs.rm(path.join(OUT_DIR, f))
    }
  }

  const files = (await fs.readdir(SRC_DIR))
    .filter((f) => f.toLowerCase().endsWith('.svg'))
    .sort()

  const seen = new Map() // kebab base → component name (collision detection)
  const exports = []

  for (const file of files) {
    const stem = file.replace(/\.svg$/i, '')
    const componentName = toComponentName(stem)
    const fileBase = toKebabBase(stem)

    if (!fileBase) {
      console.warn(`  skip empty kebab base for: ${file}`)
      continue
    }

    if (seen.has(fileBase)) {
      console.warn(
        `  duplicate kebab base "${fileBase}" — keeping ${seen.get(fileBase)}, skipping ${componentName}`,
      )
      continue
    }
    seen.set(fileBase, componentName)

    const raw = await fs.readFile(path.join(SRC_DIR, file), 'utf8')
    const jsx = rewriteSvg(raw)
    const source = buildComponentSource(componentName, jsx, file)
    const outPath = path.join(OUT_DIR, `icon-${fileBase}.tsx`)
    await fs.writeFile(outPath, source, 'utf8')

    exports.push({ componentName, fileBase })
  }

  // types.ts
  const types = `// Shared icon prop type. Use this in your own slot/wrapper components.
import type { SVGProps } from 'react'

export type IconProps = SVGProps<SVGSVGElement>
`
  await fs.writeFile(path.join(OUT_DIR, 'types.ts'), types, 'utf8')

  // index.ts (barrel) — sorted alphabetically by component name for readability
  exports.sort((a, b) => a.componentName.localeCompare(b.componentName))
  const barrel =
    `// AUTO-GENERATED. Re-run scripts/generate-icon-components.mjs to update.\n` +
    `export type { IconProps } from './types'\n` +
    exports
      .map(
        ({ componentName, fileBase }) =>
          `export { ${componentName} } from './icon-${fileBase}'`,
      )
      .join('\n') +
    '\n'
  await fs.writeFile(path.join(OUT_DIR, 'index.ts'), barrel, 'utf8')

  const ms = Date.now() - start
  console.log(
    `✓ Generated ${exports.length} icon components in ${path.relative(ROOT, OUT_DIR)} (${ms}ms)`,
  )
}

main().catch((err) => {
  console.error(err)
  process.exit(1)
})
